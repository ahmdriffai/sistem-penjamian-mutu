<h3><i class="fa fa-question-circle"></i> Tentang Penjaminan Mutu Internal</h3>
<p>
    Sesuai dengan amanat UU No. 12 Tahun 2012, bahwa penjaminan mutu perguruan tinggi ditujukan untuk pendidikan mutu. Perguruan tinggi harus secara sistematis melaksanakan proses penjaminan mutu melalui Sistem Penjaminan Mutu Perguruan Tinggi dan mengacu Standar Nasional Pendidikan Tinggi.
</p>
<p>
    Sistem penjaminan mutu internal (SPMI) adalah kegiatan sistemik penjaminan mutu pendidikan tinggi oleh setiap perguruan tinggi secara otonom untuk mengendalikan dan meningkatkan penyelenggaraan pendidikan tinggi secara berencana dan berkelanjutan. Untuk mencapai tujuan SPMI serta untuk mewujudkan visi, misi, dan tujuan universitas, maka sivitas akademika dalam melaksanakan SPMI pada setiap aras selalu berpedoman pada prinsip:
</p>
<ul>
    <li>berorientasi kepada pemangku kepentingan internal dan eksternal;</li>
    <li>mengutamakan kebenaran;</li>
    <li>tanggungjawab sosial;</li>
    <li>pengembangan kompetensi personel;</li>
    <li>partisipatif dan kolegial;</li>
    <li>keseragaman metode;</li>
    <li>inovasi, belajar dan perbaikan secara berkelanjutan.</li>
</ul>
<h5>TUJUAN DAN STRATEGI SPMI</h5>
<p>
    SPMI bertujuan untuk:
</p>
<ol>
    <li>Menjamin bahwa setiap layanan pendidikan kepada mahasiswa dilakukan sesuai standar yang ditetapkan, sehingga apabila diketahui bahwa standar tersebut tidak bermutu atau terjadi penyimpangan antara kondisi riil dengan standar akan segera diperbaiki;</li>
    <li>Mewujudkan transparasi dan akuntabilitas kepada masyarakat khususnya orang tua/wali mahasiswa, tentang penyelenggaraan pendidikan sesuai dengan standar yang ditetapkan;</li>
    <li>Mengajak semua pihak dalam universitas untuk bekerja mencapai tujuan dengan berpatokan pada standar dan secara berkelanjutan berupaya untuk meningkatkan mutu.</li>
</ol>
<p>
    Strategi di dalam melaksanakan SPMI adalah:
</p>
<ol>
    <li>Melibatkan secara aktif semua sivitas akademika sejak tahap perencanaan sehingga tahap evaluasi dan tahap pengembangan SPMI;</li>
    <li>Melibatkan pula organisasi profesi, alumni, dunia usaha dan pemerintahan sebagai pengguna lulusan, khususna pada tahap penetapan standar SPMI;</li>
    <li>Melakukan pelatihan secara terstruktur dan terencana bagi para dosen dan staf administrasi tentang SPMI, dan secara  khusus pelatihan sebagai auditor internal;</li>
    <li>Melakukan sosialisasi tentang fungsi dan tujuan SPMI kepada para pemangku kepentingan secara periodik.</li>
</ol>
